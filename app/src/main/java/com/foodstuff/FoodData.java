package com.foodstuff;

public class FoodData {
    String name;
    String image;
    String kcal;
    String carbs;
    String fat;
    String protein;


    public FoodData() {
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getKcal() {
        return kcal;
    }

    public String getCarbs() {
        return carbs;
    }

    public String getFat() {
        return fat;
    }

    public String getProtein() {
        return protein;
    }
}
